                <h2>Adventure Tips:</h2><p><li>MOTOR BIKING:
Nothing quite compares to the thrill of exploring the valley on a motorbike. With natural beauty abounding around Pokhara, the ride is adventure packed with thrills and excitement. The friendly locals make the trip delightful and one can venture anywhere in the unrestricted valley. All you need for a great time is a good bike and a reliable map.</li>
<li>BOATING:
Pokhara’s Phewa Lake naturally draws visitors towards it. Easily the best means of recreation, boating is a favorite pastime which one can enjoy any time of day. The other two lakes, Begnas and Rupa, are located 15 km out of Pokhara. Less visited but no less beautiful, both these lakes are excellent for boating and fishing.</li>
<li>PARAGLIDING:
Paragliding is perhaps the most rewarding of short duration adventure sports given the fact that there is nothing between you and the scenery unfolding before you. In the distance lie the majestic Himalayas and down below are the villages, monasteries, temples, lakes and jungles, a truly bird’s eye view and a fascinating one at that.</li>
<li>ULTRA-LIGHT AIRCRAFT:
Go up in an ultra-light aircraft and get a sweeping bird’s eye view of the entire Pokhara Valley. It is an exhilarating ride that allows unhindered views of the mountains and the scenic lakes far below. Ultra-light flights are operated from Pokhara Airport beginning September through June.</li>
<li>MOUNTAIN BIKING:
Take to the countryside where you can ride through lush green fields, delightful little hamlets, up and down the hills, along river banks, around temples, over suspension bridges and along the highway. Mountain bikes can be easily rented from any of the numerous bicycle rental shops in and around the city.</li>
<li>RAFTING:
Nepal’s rivers rank high in the world of white-water rafting, and Pokhara is ideally situated for raft trips along its rivers. The most popular river for rafting is the Trishuli River, which flows along the Kathmandu-Pokhara highway. Equally good with plenty of exciting rapids are the Kali Gandaki and Seti rivers.</li>
<li>HIKING AROUND POKHARA:
There are hiking destinations around Pokhara for those constrained by time to go on longer popular treks to the Annapurna Circuit.</li>
