                <h2>Basic Information</h2>
                
               <p>Pokhara’s bewitching beauty has been the subject of many travel writers. Its pristine air, spectacular backdrop of snowy peaks, serene lakes and surrounding greenery make it ‘the jewel in the Himalaya’, a place of remarkable natural beauty. With the magnificent Annapurna range forming the backdrop and the serenity of three major lakes – Phewa, Rupa and Begnas – Pokhara is the ultimate destination for relaxation. Pokhara Valley, gateway to the Annapurna region where many a trekker finds his Shangri-la, sits high on the list of ‘must visit’ places in Nepal.</br>Pokhara once lay on the important trade route between India and Tibet. To this day, mule trains set up camps on the city outskirts, bringing goods from remote Himalayan regions including Mustang. Gurungs and Magars, who have earned world-wide fame as fierce Gurkha warriors, are predominant here. Thakalis, indigenous of the Thak Khola region of Mustang, are known for their entrepreneurship and run tea houses along the trek routes in the Annapurna region.</br> <br><b>Mountain  Views:</b> <br>
Pokhara is blessed with stunning panoramic views of the Annapurna range which forms its backdrop. The mesmerizing Machhapuchhre also called Mt. Fishtail dominates the scenery because of its proximity to the valley and can be seen from anywhere in Pokhara. Stretching from east to west, the Annapurna massif includes Annapurna 1 to IV and Annapurna South. Further away are seen the giants, Dhaulagiri (8,167m) and Manaslu (8,163m).</br> <br><b>Phewa Lake:</b> <br>
Pokhara owes its popularity to the enchanting Phewa Lake, and along its eastern shore has grown the Lakeside or Baidam, a thriving resort town of hotels, restaurants, bars and souvenir shops that allow travelers to relax and enjoy. Phewa Lake is the largest and most beautiful of the three lakes, attracting a large number of visitors who enjoy boating on its placid waters. Boats can be hired, and many visit the island temple of Barahi in the middle of the lake.</br> <br><b>Barahi Temple:</b> <br>
Located in the middle of Phewa Lake, Barahi Temple is one the most important monuments in Pokhara. The two-storied pagoda is dedicated to the boar manifestation of Ajima who represents the female force, Shakti. Sacrifices take place here, and the largest crowds of devotees are seen on Saturdays.</br> <br><b>Seti:</b> <br>
An amazing aspect of Pokhara is the vanishing river, Seti, which goes underground and disappears in many places along its route through the city. At various points Seti is barely 2 m wide but its depth reaches an astonishing 20 m! A good spot for watching the river is Mahendra Pul at Bhimsen Chowk, a bridge near the old Mission Hospital. Here the river can be seen in all its ferocity gushing down the deep gorge that it has carved over millennia.</br> <br><b>Devi’s Fall:</b> <br>
Locally known as the Patale Chhango (Nether Fall), Devi’s Fall is a fascinating waterfall located about 2 km south-west of Pokhara Airport on the Siddhartha Highway.</br> <br><b>Mahendra Gupha:</b> <br>
This is a large limestone cave at a two-hour walk to the north of Pokhara. Locally known as the House of Bats, it is famous for its stalactites and stalagmites.</br> <br><b>World Peace Pagoda:</b <br>
The World Peace Pagoda can be seen on top of a hill on the southern fringe of Phewa Lake. It has four images of the Buddha facing the four directions. The dome-shaped pagoda is an impressive sight, and its hilltop location commands a great view. It is a great vantage point which offers spectacular views of the Annapurna.</br> <br><b>The Old Bazaar:</b> <br>
A stark contrast to Lakeside  Pokhara, the old  Pokhara Bazaar, 4 km away from Phewa Lake, is a traditional bazaar and a colorful gathering place for an ethnically diverse group of traders. The temples and monuments bear a close resemblance to the Newari architecture of the Kathmandu Valley. The old bazaar is also home to one of  Pokhara’s most important shrines, the  Bindhyabasini Mandir,  a white dome-like structure atop a hill.</p>
              
