<b>Physical Training before You Go</b></br>

Before you visit Mt. Everest, ensure you are in good physical condition. Seek medical advice in advance. If you have asthma, high blood pressure, or similar conditions you should be advised against going.

Do physical training to avoid or reduce altitude sickness before you go, and acclimate in Lhasa (3,650 m) and Shigatse (3,800 m). Dingri (4,300 m) offers a good place to stop and acclimate further if you experience discomfort on the way from Shigatse.</br>
